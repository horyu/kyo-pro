fn main() {
    input! {
        n: usize,
        m: usize,
    };
    if let Some(div) = divisors(m).into_iter().filter(|&k| n <= k).min() {
        println!("{}", m / div);
    } else {
        println!("1");
    }
}

fn divisors(n: usize) -> Vec<usize> {
    let mut divisors = Vec::new();
    for i in 1..=(f64::sqrt(n as f64) + 1e-9) as usize {
        if n % i == 0 {
            divisors.push(i);
            if i != n / i {
                divisors.push(n / i);
            }
        }
    }
    divisors.sort_unstable();
    divisors
}
