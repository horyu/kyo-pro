fn main() {
    input! {
        n: usize,
        aa: [usize; 2 * n],
    };
    let mut rs = 0;
    for (x, _, z) in aa.iter().copied().tuple_windows() {
        if x == z {
            rs += 1;
        }
    }
    println!("{rs}");
}
