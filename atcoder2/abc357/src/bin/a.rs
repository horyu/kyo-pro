use proconio::{input, marker::*};
use std::cmp::{Ordering, Reverse as R};
use std::collections::{BTreeMap, BTreeSet, BinaryHeap, HashMap, HashSet, VecDeque};

fn main() {
    input! {
        n: usize,
        m: usize,
        hh: [usize; n],
    };
    let mut rs = 0;
    let mut cnt = 0;
    for h in hh {
        cnt += h;
        if cnt <= m {
            rs += 1;
        }
    }
    println!("{rs}");
}
