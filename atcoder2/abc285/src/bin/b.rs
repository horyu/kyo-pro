fn main() {
    input! {
        n: usize,
        s: Chars,
    };
    for i in 1..n {
        let mut j = 1;
        while j + i <= n && s[j - 1] != s[i + j - 1] {
            j += 1;
        }
        let rs = j - 1;
        println!("{rs}");
    }
}
