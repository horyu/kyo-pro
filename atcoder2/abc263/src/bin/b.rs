fn main() {
    input! {
        n: usize,
        pp: [usize; n - 1],
    };
    let mut rs = 1;
    let mut p = pp[n - 2];
    while 1 < p {
        rs += 1;
        p = pp[p - 2]
    }
    println!("{rs}");
}
