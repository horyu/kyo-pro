fn main() {
    input! {
        n: usize,
        s: Chars,
    };
    let mut rs = ModInt998244353::default();
    let mut divisors = divisors(n);
    divisors.pop();
    for m in divisors {
        let mut tmp = ModInt998244353::new(1);
        for mut i in 0..m {
            let mut tk = false;
            while i < n {
                if s[i] == '.' {
                    tk = true;
                    break;
                }
                i += m;
            }
            if !tk {
                tmp *= 2;
            }
        }
        eprintln!("{m} {tmp}");
        rs += tmp;
    }
    println!("{rs}");
}

// https://qiita.com/Cassin01/items/9bc63a4bde5526150681
fn divisors(n: usize) -> Vec<usize> {
    let mut divisors = Vec::new();
    for i in 1..=(f64::sqrt(n as f64) + 1e-9) as usize {
        if n % i == 0 {
            divisors.push(i);
            if i != n / i {
                divisors.push(n / i);
            }
        }
    }
    divisors.sort_unstable();
    divisors
}
